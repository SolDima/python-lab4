# Оригінальний текст
text = "  Перші комп'ютери займали цілі кімнати, і їх обчислювальна потужність була значно меншою, ніж сучасних смартфонів. Проте ці машини стали основою для розвитку всіх сучасних технологій.   "

# Функція 1: Видалення зайвих пробілів на початку та в кінці тексту
stripped_text = text.strip()

# Функція 2: Заміна всіх входжень слова "комп'ютери" на "машини"
replaced_text = stripped_text.replace("комп'ютери", "машини")

# Функція 3: Перевірка, чи містить текст лише букви (без пробілів, цифр чи символів)
is_alpha_only = stripped_text.isalpha()

# Вивід результатів обробки
print("Текст без зайвих пробілів:", stripped_text)
print("Текст після заміни:", replaced_text)
print("Чи містить текст тільки букви:", is_alpha_only)
