# Оригінальний текст
text = "Перші комп'ютери займали цілі кімнати, і їх обчислювальна потужність була значно меншою, ніж сучасних смартфонів. Проте ці машини стали основою для розвитку всіх сучасних технологій."



# Функція 1: Переводимо весь текст у верхній регістр
upper_text = text.upper()

# Функція 2: Замінюємо слово "сучасних" на "нових"
replaced_text = upper_text.replace("СУЧАСНИХ", "НОВИХ")

# Функція 3: Рахуємо кількість входжень слова "КОМП'ЮТЕРИ"
word_count = replaced_text.count("КОМП'ЮТЕРИ")

# Виведемо результати обробки
print("Текст у верхньому регістрі:", upper_text)
print("Текст після заміни:", replaced_text)
print("Кількість входжень слова 'КОМП'ЮТЕРИ':", word_count)
